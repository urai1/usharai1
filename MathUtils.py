class MathUtils:
    @staticmethod
    def add(a, b):
        return a + b

    @staticmethod
    def subtract(a, b):
        return a - b

    @staticmethod
    def multiply(a, b):
        return a * b

    @staticmethod
    def divide(a, b):
        if b == 0:
            return -1.0
        else:
            return a / b


result_add = MathUtils.add(5, 3)
result_subtract = MathUtils.subtract(10, 4)
result_multiply = MathUtils.multiply(2, 6)
result_divide = MathUtils.divide(8, 2)

print("Addition result:", result_add)
print("Subtraction result:", result_subtract)
print("Multiplication result:", result_multiply)
print("Division result:", result_divide)
